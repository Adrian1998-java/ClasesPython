# clase animal
class Animal:
    
    def __init__(self):
        pass
    
    def hacer_sonido(self):
        print("El animal hace un sonido.")
        
class Perro(Animal):
    
    def __init__(self):
        super().__init__()
        
    def hacer_sonido(self):
        print("¡Guau Guau!")

class Gato(Animal):
    def __init__(self):
        super().__init__()
        
    def hacer_sonido(self):
        print("¡Miau Miau!")
        
class Mono(Animal):
    def __init__(self):
        super().__init__()
        
    def hacer_sonido(self):
        print("¡Uu Uu Ah Ah!")

